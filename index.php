            <p>Here are a few simple and delicious avocado recipes to try:</p>
            <ul>
                <li><strong>Avocado Toast:</strong> Mash up a ripe avocado and spread it on whole-grain toast. Top with a sprinkle of salt, pepper, and red pepper flakes for an extra kick.</li>
                <li><strong>Guacamole:</strong> Mash avocados with chopped onions, tomatoes, cilantro, lime juice, salt, and pepper for a classic dip.</li>
                <li><strong>Avocado Salad:</strong> Toss sliced avocados with mixed greens, cherry tomatoes, cucumber, and a light vinaigrette dressing for a refreshing salad.</li>
