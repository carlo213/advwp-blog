            <h2>Nutritional Benefits</h2>
            <p>Avocados offer numerous health benefits:</p>
            <ul>
                <li><strong>Healthy Fats:</strong> Avocados are rich in monounsaturated fats, particularly oleic acid, which is beneficial for heart health.</li>
                <li><strong>High in Fiber:</strong> A 100-gram serving of avocado contains around 7 grams of dietary fiber, promoting digestive health.</li>
                <li><strong>Vitamins and Minerals:</strong> Avocados are packed with essential nutrients such as vitamin K, vitamin E, vitamin C, and potassium.</li>
            </ul>
